package it.polimi.ingsw.utilities;

/**
 * Contains all the possibile endings of a game.
 */
public enum EndType {

    /**
     * The current player neither win nor lose.
     */
    DRAW,

    /**
     * The current player lost the game.
     */
    LOSE,

    /**
     * The current player won the game.
     */
    WIN
}
