package it.polimi.ingsw.view.cli.colours;

import it.polimi.ingsw.view.cli.Colour;

/**
 * House fuchsia colour.
 */
public class HouseFuchsia implements Colour {

    /**
     * Gets R value.
     *
     * @return R value.
     */
    @Override
    public int getR() {
        return 221;
    }

    /**
     * Gets G value.
     *
     * @return G value.
     */
    @Override
    public int getG() {
        return 61;
    }

    /**
     * Gets B value.
     *
     * @return B value.
     */
    @Override
    public int getB() {
        return 165;
    }
}
