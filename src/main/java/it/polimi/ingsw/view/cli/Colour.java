package it.polimi.ingsw.view.cli;

/**
 * Colour interface for CLI.
 */
public interface Colour {

    /**
     * Gets R value.
     *
     * @return R value.
     */
    int getR();

    /**
     * Gets G value.
     *
     * @return G value.
     */
    int getG();

    /**
     * Gets B value.
     *
     * @return B value.
     */
    int getB();
}
